sub getRepo($) {
    my $repo = shift;

    if (!-e $repo) {
        system ("git clone https://github.com/GutenbergSource/$repo.git\n");
    } elsif (-d $repo) {
        my $cwd = getcwd;
        chdir ($repo);
        system ("git pull\n");
        chdir $cwd;
    }
}

